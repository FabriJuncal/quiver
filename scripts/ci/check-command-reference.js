#!/usr/bin/env node

const cp = require('node:child_process');
const fs = require('node:fs');
const path = require('node:path');
const vm = require('node:vm');

const DEFAULT_DOC_PATH = 'docs/reference/commands.md';
const DEFAULT_SOURCE_PATH = 'src/create-quiver/index.js';
const START_MARKER = '<!-- quiver:generated-cli-reference:start -->';
const END_MARKER = '<!-- quiver:generated-cli-reference:end -->';

function repoRootFromScript() {
  return path.resolve(__dirname, '..', '..');
}

function stripAnsi(value) {
  return String(value || '').replace(/\u001b\[[0-?]*[ -/]*[@-~]/g, '');
}

function extractArrayLiteral(source, constName) {
  const assignment = `const ${constName} =`;
  const assignmentIndex = source.indexOf(assignment);
  if (assignmentIndex === -1) {
    throw new Error(`Could not find ${constName} in CLI source.`);
  }

  const arrayStart = source.indexOf('[', assignmentIndex + assignment.length);
  if (arrayStart === -1) {
    throw new Error(`Could not find ${constName} array literal.`);
  }

  let depth = 0;
  let quote = null;
  let escaped = false;

  for (let index = arrayStart; index < source.length; index += 1) {
    const char = source[index];

    if (quote) {
      if (escaped) {
        escaped = false;
      } else if (char === '\\') {
        escaped = true;
      } else if (char === quote) {
        quote = null;
      }
      continue;
    }

    if (char === '\'' || char === '"' || char === '`') {
      quote = char;
      continue;
    }

    if (char === '[') {
      depth += 1;
    } else if (char === ']') {
      depth -= 1;
      if (depth === 0) {
        return source.slice(arrayStart, index + 1);
      }
    }
  }

  throw new Error(`Could not parse ${constName} array literal.`);
}

function validateCommandHelpGroups(groups) {
  if (!Array.isArray(groups) || groups.length === 0) {
    throw new Error('COMMAND_HELP_GROUPS must be a non-empty array.');
  }

  for (const group of groups) {
    if (!group || typeof group.title !== 'string' || !Array.isArray(group.commands)) {
      throw new Error('Every command help group must have a title and commands array.');
    }

    for (const entry of group.commands) {
      if (!Array.isArray(entry) || entry.length !== 2 || typeof entry[0] !== 'string' || typeof entry[1] !== 'string') {
        throw new Error(`Invalid command help entry in group "${group.title}".`);
      }
    }
  }
}

function loadCommandHelpGroups(repoRoot, sourcePath = DEFAULT_SOURCE_PATH) {
  const source = fs.readFileSync(path.join(repoRoot, sourcePath), 'utf8');
  const literal = extractArrayLiteral(source, 'COMMAND_HELP_GROUPS');
  const groups = vm.runInNewContext(literal, Object.create(null), { timeout: 1000 });
  validateCommandHelpGroups(groups);
  return groups;
}

function runRuntimeHelp(repoRoot) {
  const result = cp.spawnSync(process.execPath, ['bin/create-quiver.js', '--lang', 'en', '--help', '--no-color'], {
    cwd: repoRoot,
    encoding: 'utf8',
    env: {
      ...process.env,
      FORCE_COLOR: '0',
      NO_COLOR: '1',
      QUIVER_LANG: 'en',
    },
    shell: false,
  });

  if (result.error) {
    throw result.error;
  }

  if (result.status !== 0) {
    throw new Error(`CLI help command failed with exit code ${result.status}:\n${result.stderr || result.stdout}`);
  }

  return stripAnsi(result.stdout);
}

function assertGeneratedGroupsMatchRuntimeHelp(groups, runtimeHelp) {
  const missing = [];

  for (const group of groups) {
    if (!runtimeHelp.includes(`${group.title}:`)) {
      missing.push(`group "${group.title}"`);
    }

    for (const [command, description] of group.commands) {
      if (!runtimeHelp.includes(command)) {
        missing.push(`command "${command}"`);
      }
      if (!runtimeHelp.includes(description)) {
        missing.push(`description for "${command}"`);
      }
    }
  }

  if (missing.length > 0) {
    throw new Error(`Runtime help is missing generated reference entries:\n- ${missing.join('\n- ')}`);
  }
}

function escapeMarkdownTableCell(value) {
  return String(value)
    .replace(/\r?\n/g, ' ')
    .replace(/\|/g, '\\|')
    .trim();
}

function countCommands(groups) {
  return groups.reduce((total, group) => total + group.commands.length, 0);
}

function renderGeneratedCommandReference(groups) {
  const lines = [
    START_MARKER,
    '> Generated by `npm run docs:commands:write` from `COMMAND_HELP_GROUPS` and checked against `node bin/create-quiver.js --lang en --help --no-color`. Do not edit this block manually.',
    '',
    '| Group | Command | Runtime help |',
    '|---|---|---|',
  ];

  for (const group of groups) {
    for (const [command, description] of group.commands) {
      lines.push(`| ${escapeMarkdownTableCell(group.title)} | \`${escapeMarkdownTableCell(command)}\` | ${escapeMarkdownTableCell(description)} |`);
    }
  }

  lines.push(END_MARKER);
  return lines.join('\n');
}

function buildGeneratedCommandReference(repoRoot) {
  const groups = loadCommandHelpGroups(repoRoot);
  const runtimeHelp = runRuntimeHelp(repoRoot);
  assertGeneratedGroupsMatchRuntimeHelp(groups, runtimeHelp);
  return {
    block: renderGeneratedCommandReference(groups),
    commandCount: countCommands(groups),
    groupCount: groups.length,
  };
}

function findGeneratedBlockBounds(docText) {
  const start = docText.indexOf(START_MARKER);
  const end = docText.indexOf(END_MARKER);

  if (start === -1 && end === -1) {
    return null;
  }

  if (start === -1 || end === -1 || end < start) {
    throw new Error('Generated CLI reference markers are incomplete or out of order.');
  }

  return {
    start,
    end: end + END_MARKER.length,
  };
}

function extractGeneratedBlock(docText) {
  const bounds = findGeneratedBlockBounds(docText);
  if (!bounds) {
    return null;
  }
  return docText.slice(bounds.start, bounds.end);
}

function insertGeneratedSection(docText, generatedBlock) {
  const heading = '## Referencia generada desde el CLI';
  const intro = [
    'El bloque siguiente se actualiza con `npm run docs:commands:write` y se verifica con `npm run docs:commands:check`.',
    'El contenido curado fuera de los marcadores se mantiene manual y no debe ser reescrito por el generador.',
  ].join('\n\n');
  const section = `${heading}\n\n${intro}\n\n${generatedBlock}`;
  const insertionPoint = docText.indexOf('\n## Configuración inicial');

  if (insertionPoint === -1) {
    return `${docText.trimEnd()}\n\n${section}\n`;
  }

  return `${docText.slice(0, insertionPoint)}\n${section}\n${docText.slice(insertionPoint)}`;
}

function replaceGeneratedBlock(docText, generatedBlock) {
  const bounds = findGeneratedBlockBounds(docText);
  if (!bounds) {
    return insertGeneratedSection(docText, generatedBlock);
  }

  return `${docText.slice(0, bounds.start)}${generatedBlock}${docText.slice(bounds.end)}`;
}

function runCommandReferenceCheck(options = {}) {
  const repoRoot = options.repoRoot || repoRootFromScript();
  const docPath = options.docPath || DEFAULT_DOC_PATH;
  const docFullPath = path.join(repoRoot, docPath);
  const write = Boolean(options.write);
  const generated = buildGeneratedCommandReference(repoRoot);
  const currentDoc = fs.readFileSync(docFullPath, 'utf8');

  if (write) {
    const updatedDoc = replaceGeneratedBlock(currentDoc, generated.block);
    if (updatedDoc !== currentDoc) {
      fs.writeFileSync(docFullPath, updatedDoc);
    }
    return {
      ok: true,
      mode: 'write',
      changed: updatedDoc !== currentDoc,
      docPath,
      commandCount: generated.commandCount,
      groupCount: generated.groupCount,
    };
  }

  const currentBlock = extractGeneratedBlock(currentDoc);
  if (!currentBlock) {
    return {
      ok: false,
      mode: 'check',
      reason: `Generated CLI reference markers are missing from ${docPath}. Run npm run docs:commands:write.`,
      docPath,
      commandCount: generated.commandCount,
      groupCount: generated.groupCount,
    };
  }

  if (currentBlock !== generated.block) {
    return {
      ok: false,
      mode: 'check',
      reason: `Generated CLI reference is stale in ${docPath}. Run npm run docs:commands:write.`,
      docPath,
      commandCount: generated.commandCount,
      groupCount: generated.groupCount,
    };
  }

  return {
    ok: true,
    mode: 'check',
    docPath,
    commandCount: generated.commandCount,
    groupCount: generated.groupCount,
  };
}

function formatReport(report) {
  const lines = [
    'CLI command reference',
    `Mode: ${report.mode}`,
    `Docs file: ${report.docPath}`,
    `Groups: ${report.groupCount}`,
    `Commands: ${report.commandCount}`,
  ];

  if (Object.prototype.hasOwnProperty.call(report, 'changed')) {
    lines.push(`Changed: ${report.changed ? 'yes' : 'no'}`);
  }

  if (report.reason) {
    lines.push(`Reason: ${report.reason}`);
  }

  lines.push(report.ok ? 'PASS: command reference is synchronized.' : 'FAIL: command reference drift detected.');
  return `${lines.join('\n')}\n`;
}

function parseArgs(args) {
  const write = args.includes('--write');
  const check = args.includes('--check');
  const help = args.includes('--help') || args.includes('-h');

  if (help) {
    return { help: true };
  }

  if (write && check) {
    throw new Error('Use either --write or --check, not both.');
  }

  return { write };
}

if (require.main === module) {
  try {
    const args = parseArgs(process.argv.slice(2));
    if (args.help) {
      process.stdout.write('Usage: node scripts/ci/check-command-reference.js [--check|--write]\n');
      process.exit(0);
    }

    const report = runCommandReferenceCheck({ write: args.write });
    process.stdout.write(formatReport(report));
    if (!report.ok) {
      process.exit(1);
    }
  } catch (error) {
    console.error(error.message);
    process.exit(1);
  }
}

module.exports = {
  assertGeneratedGroupsMatchRuntimeHelp,
  buildGeneratedCommandReference,
  extractGeneratedBlock,
  loadCommandHelpGroups,
  renderGeneratedCommandReference,
  replaceGeneratedBlock,
  runCommandReferenceCheck,
  runRuntimeHelp,
  START_MARKER,
  END_MARKER,
};
